                     USDA Reports Record Amounts of Spoilage in Stored Grain in 2010
                  </span>
                </p>

                <p>
                    Millions of dollars are lost worldwide due to insect and fungi caused spoilage in stored grain.
                    The predominate method for spoilage detection at farms involves manual walking, smelling,
                    sampling the grain inside the storage bin which is at best very time consuming and randomly
                    effective and at worst life threatening. The predominate method for detection at
                    elevators/processors
                    is via temperature cables which have significant efficacy problems due to the insulation value
                    of grain that hampers hot spot detection.
                </p>

                <p>
                    We propose to develop new technology for early
                    detection that takes advantage of the fact that small amounts of spoilage cause significant
                    increases in CO2 concentration throughout the grain mass and storage structure. A sensor
                    system designed to detect the early and progressive stages of spoilage before a human or
                    temperature cable response. The new sensor will be designed for placement in stored grain
                    structures to continuously monitor CO2. The readings will be processed intelligently to
                    give users information about the characteristics and extent of the spoilage for their
                    quality management decision making process. This will require the development of inexpensive,
                    easy to install, easy to use, reliable sensor hardware to withstand the harsh grain
                    facility environment. And software that accounts for dynamic factors such as grain type,
                    grain quantity, storage structure architecture; to effectively alert the user about
                    developing decay.
                </p>
